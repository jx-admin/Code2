
package com.act.mbanking.bean;

public class BankInformationModel {
    private String srcBankName;

    private String srcBranchName;

    private String destBankName;

    private String destBranchName;

    public String getSrcBankName() {
        return srcBankName;
    }

    public void setSrcBankName(String srcBankName) {
        this.srcBankName = srcBankName;
    }

    public String getDestBankName() {
        return destBankName;
    }

    public void setDestBankName(String destBankName) {
        this.destBankName = destBankName;
    }

    public String getSrcBranchName() {
        return srcBranchName;
    }

    public void setSrcBranchName(String srcBranchName) {
        this.srcBranchName = srcBranchName;
    }

    public String getDestBranchName() {
        return destBranchName;
    }

    public void setDestBranchName(String destBranchName) {
        this.destBranchName = destBranchName;
    }

}
