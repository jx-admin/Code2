package com.act.sctc.been;

public class UnitHome {
	private int id;
	private int type;
	private String home_img;
	private String home_video;
	private String title;
	private String icon;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public String getHomImg() {
		return home_img;
	}
	public void setHomeImg(String homeImg) {
		this.home_img = homeImg;
	}
	public String getHomeVideo() {
		return home_video;
	}
	public void setHomeVideo(String homeVideo) {
		this.home_video = homeVideo;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getIcon() {
		return icon;
	}
	public void setIcon(String icon) {
		this.icon = icon;
	}
}
